using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace TextXUnit
{
    public class Calculadora
    {
        public static float somar(float v1, float v2)
        {
            return v1 + v2;
        }
        public static float subtrair(float v1, float v2)
        {
            return v1 - v2;
        }
        public static float multiplicar(float v1, float v2)
        {
            return v1 * v2;
        }
        public static float dividir(float v1, float v2)
        {
            return v1 / v2;
        }

    }
}
